CREATE TABLE IF NOT EXISTS users (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email                VARCHAR(255) NOT NULL UNIQUE,
    password             VARCHAR(255) NOT NULL,
    name                 VARCHAR(100) NOT NULL,
    system_role          VARCHAR(20)  NOT NULL DEFAULT 'USER',
    storage_limit_bytes  BIGINT       NOT NULL DEFAULT 16106127360,
    storage_used_bytes   BIGINT       NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS files (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(512)  NOT NULL,
    owner_id     BIGINT        NOT NULL REFERENCES users(id),
    parent_id    BIGINT,
    type         VARCHAR(10)   NOT NULL,          -- FILE | FOLDER
    mime_type    VARCHAR(255),                    -- 폴더면 NULL
    storage_path VARCHAR(1024),                   -- 폴더면 NULL
    size_bytes   BIGINT        NOT NULL DEFAULT 0,
    is_trashed   BOOLEAN       NOT NULL DEFAULT FALSE,
    trashed_at   TIMESTAMP,
    is_starred   BOOLEAN       NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS user_groups (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(255) NOT NULL,
    description VARCHAR(1000),
    created_by  BIGINT       NOT NULL REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS group_members (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id    BIGINT      NOT NULL REFERENCES user_groups(id) ON DELETE CASCADE,
    user_id     BIGINT      NOT NULL REFERENCES users(id),
    group_role  VARCHAR(20) NOT NULL DEFAULT 'MEMBER',
    UNIQUE (group_id, user_id)
);

CREATE TABLE IF NOT EXISTS group_invitations (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id    BIGINT      NOT NULL REFERENCES user_groups(id) ON DELETE CASCADE,
    inviter_id  BIGINT      NOT NULL REFERENCES users(id),
    invitee_id  BIGINT      NOT NULL REFERENCES users(id),
    status      VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    created_at  TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS file_permissions (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    file_id              BIGINT       NOT NULL REFERENCES files(id) ON DELETE CASCADE,
    granted_by_id        BIGINT       NOT NULL REFERENCES users(id),
    user_id              BIGINT,
    group_id             BIGINT,
    share_token          VARCHAR(128) UNIQUE,
    permission           VARCHAR(20)  NOT NULL,   -- VIEWER | EDITOR
    inherit_to_children  BOOLEAN      NOT NULL DEFAULT TRUE,
    expires_at           TIMESTAMP
);
